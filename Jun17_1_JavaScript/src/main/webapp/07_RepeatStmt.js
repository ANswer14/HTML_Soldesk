// 7. 반복문  (Java와 동일)
//	동일한 작업을 여러번 반복할 때 사용

// 1 ~ 10 까지 출력(document.write())

// for
for(let i = 1; i < 11; i++){
	document.write(i);
}
// while
let q = 0;
while(q < 10){
	q++;
	document.write(q + "<br>");
}

// do-while
let w = 1;
do {
	document.write(w + "<br>");
	w++;
} while(w < 11);


